
public class islem {
	
	public int topla(int sayi1, int sayi2)
	{
		int sonuc = sayi1+sayi2;
		return sonuc;
	}
	public int cikar(int sayi1, int sayi2)
	{
		int sonuc = sayi1-sayi2;
		return sonuc;
	}
	public int carp(int sayi1, int sayi2)
	{
		int sonuc = sayi1*sayi2;
		return sonuc;
	}
	public double bol(double sayi1, double sayi2)
	{
		double sonuc = sayi1/sayi2;
		return sonuc;
	}

}
